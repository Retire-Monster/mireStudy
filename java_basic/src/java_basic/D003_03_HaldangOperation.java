package java_basic;

public class D003_03_HaldangOperation {

	public static void main(String[] args) {

		int a = 12;
		int b = 10;

		a += 10; // a = a + 10
		b -= 20; // b = b - 20

		System.out.println(a);
		System.out.println(b);

		char c1 = 'A';
		char c2 = 'a';

		System.out.println(c1 > c2);
		System.out.println(c1 > 45);
		System.out.println(c1 > 1.5);

		System.out.println((char) (c1 + 3));	//문자의 연산

	}

}
